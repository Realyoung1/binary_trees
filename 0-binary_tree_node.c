/**
  * binary_tree_node - funcs creatings a new node of a Binary trees
  * @parent: node parentss
  * @value: keyss to inserts in the new nodess
  * Return: the addresss of the new nodess
  */

binary_tree_t *binary_tree_node(binary_tree_t *parent, int value)
{
	binary_tree_t *new_node = NULL;

	new_node = malloc(sizeof(binary_tree_t));
	if (!new_node)
		return (NULL);

	new_node->parent = parent;
	new_node->n = value;
	new_node->left = NULL;
	new_node->right = NULL;

	return (new_node);
}
